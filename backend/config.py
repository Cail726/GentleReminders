def interpret_dimension(avg: float) -> dict:
    """将量表维度平均分转换为等级描述"""
    if avg <= 2.0:
        return {"level": "green", "label": "状态良好", "desc": "该维度表现良好，请继续保持当前的生活方式。"}
    elif avg <= 3.0:
        return {"level": "yellow", "label": "轻度关注", "desc": "该维度有一些轻微困扰，建议适当关注和自我调整。"}
    elif avg <= 4.0:
        return {"level": "orange", "label": "值得注意", "desc": "该维度存在较明显的困扰，建议主动寻求改善方法。"}
    else:
        return {"level": "red", "label": "需要关注", "desc": "该维度困扰程度较高，建议与信任的人交流或寻求心理咨询。"}
