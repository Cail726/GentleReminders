def interpret_dimension(avg: float, dim: str = None) -> dict:
    """将量表维度平均分转换为等级描述，维度感知的个性化建议"""
    dim = dim or ""
    tips = _DIMENSION_TIPS.get(dim, _DEFAULT_TIPS)
    advice = _DIMENSION_ADVICE.get(dim, _DEFAULT_ADVICE)
    if avg <= 2.0:
        idx = 0
    elif avg <= 3.0:
        idx = 1
    elif avg <= 4.0:
        idx = 2
    else:
        idx = 3
    return {
        "level": ["green", "yellow", "orange", "red"][idx],
        "label": ["状态良好", "轻度关注", "值得注意", "需要关注"][idx],
        "desc": tips[idx],
        "advice": advice[idx],
    }


